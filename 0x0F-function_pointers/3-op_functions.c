# include "3-calc.h"
/**
 * op_add - addition
 * @a: input
 * @b: input
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - addition
 * @a: input
 * @b: input
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - addition
 * @a: input
 * @b: input
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - addition
 * @a: input
 * @b: input
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - addition
 * @a: input
 * @b: input
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
